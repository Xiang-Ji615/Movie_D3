package main.java.movie.dao.model;

public class MovieGrossType {

	Long grossSum;
	String type;
	String name;

	public Long getGrossSum() {
		return grossSum;
	}

	public void setGrossSum(Long grossSum) {
		this.grossSum = grossSum;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
